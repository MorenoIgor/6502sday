STARTUP: 
LDA #1    
STA $00 ;X1
STA $01 ;Y1
LDA #5
STA $02 ;W1
STA $03 ;H1

LDA #4 
STA $04 ;X2
STA $05 ;Y2
LDA #5
STA $06 ;W2
STA $07 ;H2

JSR CHECK_COLLISION
 
CHECK_COLLISION:
CLC
LDX #1 
PASS:
CLC
LDA $04,X
ADC $06,X
CMP $00,X
BCC NO_COLLISION
CLC
LDA $00,X
ADC $02,X
CMP $04,X
BCC NO_COLLISION
DEX
BEQ PASS 

COLLISION:
LDA #5
STA $200
JMP END

NO_COLLISION:
END:
RTS
